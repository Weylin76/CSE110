# Regular Colors
black = '\033[0;30m'
red = '\033[0;31m'
green = '\033[0;32m'
yellow = '\033[0;33m'
blue = '\033[0;34m'
magenta = '\033[0;35m'
cyan = '\033[0;36m'
white = '\033[0;37m'

# Bold Colors
bold_black = '\033[1;30m'
bold_red = '\033[1;31m'
bold_green = '\033[1;32m'
bold_yellow = '\033[1;33m'
bold_blue = '\033[1;34m'
bold_magenta = '\033[1;35m'
bold_cyan = '\033[1;36m'
bold_white = '\033[1;37m'

# Underline Colors
underline_black = '\033[4;30m'
underline_red = '\033[4;31m'
underline_green = '\033[4;32m'
underline_yellow = '\033[4;33m'
underline_blue = '\033[4;34m'
underline_magenta = '\033[4;35m'
underline_cyan = '\033[4;36m'
underline_white = '\033[4;37m'

# Background Colors
bg_black = '\033[40m'
bg_red = '\033[41m'
bg_green = '\033[42m'
bg_yellow = '\033[43m'
bg_blue = '\033[44m'
bg_magenta = '\033[45m'
bg_cyan = '\033[46m'
bg_white = '\033[47m'

# Reset Formatting
reset = '\033[0m'

# Example usage
print(black + 'Black' + reset)
print(red + 'Red' + reset)
print(green + 'Green' + reset)
print(yellow + 'Yellow' + reset)
print(blue + 'Blue' + reset)
print(magenta + 'Magenta' + reset)
print(cyan + 'Cyan' + reset)
print(white + 'White' + reset)
